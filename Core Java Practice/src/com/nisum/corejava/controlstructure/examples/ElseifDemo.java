package com.nisum.corejava.controlstructure.examples;

public class ElseifDemo {
	public static void main(String[] args) {
		int a = 30;
		if (a == 10) {
			System.out.println("the value of a is 10");
		} else if (a == 20) {
			System.out.println("the value is 20");
		} else if (a == 30) {
			System.out.println("the value is 30");
		} else {
			System.out.println("print the else");
		}

	}

}
